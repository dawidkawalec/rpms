                <div class="col-lg-12">
                    <p class="bold-text">Wypłata środków z PPK – kiedy i w jaki sposób?</p>
                    <p>
                        Zachętą do wzięcia udziału w programie PPK ma być jego prosta, nieskomplikowana forma, czyli
                        automatyczny zapis i brak zbędnych formalności. Dzięki tym zaletom, według wstępnych prognoz ma
                        zgromadzić chętnych w liczbie sięgającej aż
                        11,5 mln uczestników.
                    </p>
                    <p>Myślą przewodnią PPK i podstawowym celem programu jest możliwość regularnego, wieloletniego
                        gromadzenia środków przez pracowników. Mogą dokonać ich wypłaty po osiągnięciu 60. roku życia.
                        Obywa się to poprzez:</p>
                    <ul class="special-list">
                        <li>Jednorazową wypłatę maks. 25% uzyskanych oszczędności oraz rozłożenie pozostałych środków
                            (75%) na minimalnie 120 rat realizowanych co miesiąc.</li>
                        <li>
                            Wypłatę kwoty większej niż 25%, np. jej całości, albo w czasie krótszym niż 10 lat (nawet
                            jednorazowo). Należy pamiętać, że w takiej sytuacji obowiązkowe będzie opłacenie podatku od
                            zysków kapitałowych od nadwyżki wynoszącej
                            więcej niż 25%.
                        </li>
                    </ul>
                    <p>Istnieje również możliwość wypłaty tych środków przed ukończeniem 60. roku życia. Są to
                        wymienione przez ustawę, określone sytuacje życiowe, takie jak:</p>
                    <ul class="special-list">
                        <li>Poważna choroba osoby biorącej udział w PPK, jej małżonka lub dziecka. W takim przypadku
                            można wykorzystać 25% odłożonej sumy (bez obowiązku zwrotu),</li>
                        <li>
                            Kupno mieszkania albo budowa domu na kredyt. W tej sytuacji możemy pobrać aż 100% zebranej
                            kwoty. Jednak takie rozwiązanie jest dostępne wyłącznie dla osób przed 45. rokiem życia. Co
                            więcej, należy zwrócić całą sumę w okresie
                            maksymalnie 15 lat od dnia wybrania środków.
                        </li>
                    </ul>
                </div>
